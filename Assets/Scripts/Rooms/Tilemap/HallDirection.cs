// <summary>
/// Направление ориентации коридора (холла) на тайлмапе.
/// </summary>
public enum HallDirection
{
    /// <summary>
    /// Вертикальный коридор (простирается вдоль оси Y).
    /// </summary>
    Vertical = 0,


    /// <summary>
    /// Горизонтальный коридор (простирается вдоль оси X).
    /// </summary>
    Horizontal = 1
}
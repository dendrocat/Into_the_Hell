/// <summary>
/// Определяет категории оружия, доступные игрокам.
/// </summary>
public enum WeaponType
{
    /// <summary>
    /// Стандартный одноручное меч и щит.
    /// </summary>
    Sword = 0,

    /// <summary>
    /// Тяжёлое двуручное оружие с высоким уроном.
    /// </summary>
    TwoHandedSword = 1,

    /// <summary>
    /// Дальнобойное оружие, использующее стрелы.
    /// </summary>
    Bow = 2
}